class Pet {
    constructor(name) {
        this.name = name;
    }

    constructor(level) {
        this.level = level;
    }

    constructor(health) {
        this.health = health;
    }

    constructor(mana) {
        this.mana = mana;
    }

    constructor(attack) {
        this.attack = attack;
    }    

    constructor(defence) {
        this.defence = defence;
    }

    constructor(speed) {
        this.speed = speed;
    }    

    constructor(energy) {
        this.energy = energy;
    }

    constructor(hunger) {
        this.hunger = hunger;
    }    

    constructor(happiness) {
        this.happiness = happiness;
    }           
}